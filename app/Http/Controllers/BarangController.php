    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        DB::table('barang')->where('id', $request->id)->update([
            'kode' => $request->kode,
            'nama' => $request->nama,
